    private void jBtnapproveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnapproveActionPerformed
        
        Stock stock = new Stock();
        if(listingRequestTable.getSelectedRow()==-1)
        {
                JOptionPane.showMessageDialog(this, "Please select a row to process");
                return;

        }
        
        String selectedCompanyName=String.valueOf(listingRequestTable.getValueAt(listingRequestTable.getSelectedRow(), 1));
        Company selectedCompany = this.business.getCompliance().getComplianceModerator().getCompanyByName(selectedCompanyName);
        stock.setCompany(selectedCompany);
        selectedCompany.setLicenseRequestStatus("Listed");
        ListingRequest selectedRequest = this.stockMarketModerator.getListingRequestById(Integer.parseInt(String.valueOf(listingRequestTable.getValueAt(listingRequestTable.getSelectedRow(), 0))));
        selectedRequest.setStatus("Listed");
        selectedCompany.setListingRequestStatus("Listed");
        
        stock.setPrice(selectedRequest.getSharePrice());
        stock.setStockQuantity(selectedRequest.getTotalShares());
        stock.setAvailableQuantity(selectedRequest.getTotalShares());
        this.business.getMarket().getStockMarket().getStockList().add(stock);
        this.stockMarketModerator.getListingRequestList().remove(selectedRequest);
        
        stock.getPriceHistory().add(stock.getPrice());
        JOptionPane.showMessageDialog(this, "Listing Request Approved!");
        business = DataContext.setBusiness(business);
        populateListingTable();
    }//GEN-LAST:event_jBtnapproveActionPerformed

