    private void populateListingTable() {
        DefaultTableModel model = (DefaultTableModel) listingRequestTable.getModel();
        model.setRowCount(0);
        for(ListingRequest listingRequest: this.stockMarketModerator.getListingRequestList()){
            Object[] row= new Object[6];
            row[0]=listingRequest.getRequestId();
            row[1]=listingRequest.getCompany().getName();
            row[2]=listingRequest.getSharePrice();
            row[3]=listingRequest.getTotalShares();
            row[4]=listingRequest.getCompany().getCapital();
            row[5]=listingRequest.getStatus();
            model.addRow(row);
        }
    }
    private void updateAllEquityPrice() {
        for(Company company: this.business.getCompliance().getComplianceModerator().getCompanyList()){
            for(StockHoldings stockHoldings:company.getStockHoldings()){
                if(stockHoldings.getStock()==this.selectedStock){
                    for(Stock stock:this.business.getMarket().getStockMarket().getStockList()){
                        if(company==stock.getCompany()){
                            stock.calculateAndSetPrice();
                        }
                    }
                }
            }
        }
    }
    public double calculateMarketIndex(){
        double marketIndex=0;
        int indexWeightage=0;
        for(Stock equity:this.business.getMarket().getStockMarket().getStockList()){
            if(equity.getCompany().getCapital()<this.business.getCompliance().getComplianceModerator().getSmallCap()){
                marketIndex+=equity.getPrice();
                indexWeightage+=1;
            }
            else if(equity.getCompany().getCapital()<this.business.getCompliance().getComplianceModerator().getMediumCap()){
                marketIndex+=(equity.getPrice()*2);
                indexWeightage+=2;
            }
            else{
                marketIndex+=(equity.getPrice()*3);
                indexWeightage+=3;
            }
        }
        return marketIndex/indexWeightage;
    }

