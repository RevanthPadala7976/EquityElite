    private void populateTableShares() {
        int rc = tableShares.getRowCount();
        int i;
        for (i = rc - 1; i >= 0; i--) {
            ((DefaultTableModel) tableShares.getModel()).removeRow(i);
        }
        DefaultTableModel model = (DefaultTableModel) tableShares.getModel();
        model.setRowCount(0);
        for(Stock stock: this.business.getMarket().getStockMarket().getStockList()){
            Object[] row= new Object[6];
            row[0]=stock.getCompany().getName();
            row[1]=stock.getPrice();
            row[2]=stock.getStockQuantity();
            row[3]=stock.getCompany().getCapital();
            row[4]=stock.getCompany().getAssets();
            row[5]=stock.getCompany().getLiabilites();
            model.addRow(row);
        }
    }

