    private void populateJobsTable() {
        DefaultTableModel model = (DefaultTableModel) tblJobs.getModel();
        model.setRowCount(0);
        for (Company company : this.business.getCompliance().getComplianceModerator().getCompanyList()) {
            if (company.getInvestmentManager() == null) {
                Object[] row = new Object[5];
                row[0] = company.getName();
                row[1] = company.getCapital();
                row[2] = company.getAssets();
                row[3] = company.getLiabilites();
                model.addRow(row);
            }
        }
    }

    private void populateHoldingsTable() {
        DefaultTableModel model = (DefaultTableModel) myEquityHoldings.getModel();
        model.setRowCount(0);
        double totalInvested = 0;
        double totalValuation = 0;
        for (StockHoldings stockHoldings : this.investmentManager.getAssignedCompany().getStockHoldings()) {
            Object[] row = new Object[6];
            row[0] = stockHoldings.getStock().getCompany().getName();
            row[1] = stockHoldings.getQuantity();
            row[2] = stockHoldings.getActualPrice();
            row[3] = stockHoldings.getStock().getPrice();
            totalInvested += stockHoldings.getQuantity() * stockHoldings.getActualPrice();
            row[4] = stockHoldings.getQuantity() * stockHoldings.getActualPrice();
            totalValuation += stockHoldings.getQuantity() * stockHoldings.getStock().getPrice();
            row[5] = stockHoldings.getQuantity() * stockHoldings.getStock().getPrice();
            model.addRow(row);
        }

    }
        
        
    private void populateStockListTable() {
        DefaultTableModel model = (DefaultTableModel) tblStockListBuy.getModel();
        model.setRowCount(0);
        for (Stock stock : this.business.getMarket().getStockMarket().getStockList()) {
            Object[] row = new Object[3];
            row[0] = stock.getCompany().getName();
            row[1] = stock.getPrice();
            row[2] = stock.getCompany().getCapital();
            model.addRow(row);
        }

    }

