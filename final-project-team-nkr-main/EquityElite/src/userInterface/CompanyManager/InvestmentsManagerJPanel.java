    private void btnSellShareActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSellShareActionPerformed
        try{
            StockHoldings equityStockToSell = new StockHoldings();
            for (StockHoldings stockHoldings : this.investmentManager.getAssignedCompany().getStockHoldings()) {
                if (stockHoldings.getStock() == this.selectedStock) {
                    equityStockToSell = stockHoldings;
                }
            }
            if (Integer.parseInt(txtQtyShare1.getText()) <= equityStockToSell.getQuantity()) {
                StockSellRequest stockSellRequest = new StockSellRequest();
                stockSellRequest.setStock(selectedStock);
                stockSellRequest.setQuantity(Integer.parseInt(txtQtyShare1.getText()));
                stockSellRequest.setSeller(this.investmentManager.getAssignedCompany());
                stockSellRequest.setSaleID(this.business.getBrokerage().getSellRequestCount());
                this.business.getBrokerage().setSellRequestCount(this.business.getBrokerage().getSellRequestCount() + 1);
                StockBroker stockBroker = this.business.getBrokerage().getStockBrokerByName(String.valueOf(jComboBox2.getSelectedItem()));
                stockBroker.getSellRequests().add(stockSellRequest);
                this.investmentManager.getAssignedCompany().setBalance(this.investmentManager.getAssignedCompany().getBalance() + (Integer.parseInt(txtQtyShare1.getText()) * this.selectedStock.getPrice()));
                setAvailableBalance(this.investmentManager.getAssignedCompany().getBalance());
                this.investmentManager.getAssignedCompany().getStockSellRequests().add(stockSellRequest);
                JOptionPane.showMessageDialog(this, "Sell Request Sent!");
            } else {
                JOptionPane.showMessageDialog(this, "You do not have sufficient shares");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Oops something went wrong!");
        }
    }//GEN-LAST:event_btnSellShareActionPerformed

