    private void populateBuyRequestsTable() {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.setRowCount(0);
        for (StockPurchaseRequest stockBuyRequest : this.investmentManager.getAssignedCompany().getStockBuyRequests()) {
            Object[] row = new Object[4];
            row[0] = stockBuyRequest.getPurchaseID();
            row[1] = stockBuyRequest.getStock().getCompany().getName();
            row[2] = stockBuyRequest.getQuantity();
            row[3] = stockBuyRequest.getQuantity() * stockBuyRequest.getStock().getPrice();
            model.addRow(row);
        }
        
       
    }

