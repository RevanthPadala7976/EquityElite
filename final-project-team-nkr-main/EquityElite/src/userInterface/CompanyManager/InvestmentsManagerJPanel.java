    private void btnBuyShare1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuyShare1ActionPerformed

            try{
        int qty = Integer.parseInt(String.valueOf(txtQtyShare.getText()));
        double price = qty * selectedStock.getPrice();
        if (price < this.investmentManager.getAssignedCompany().getCapital()) {
            this.investmentManager.getAssignedCompany().setBalance(this.investmentManager.getAssignedCompany().getBalance() - price);
            StockPurchaseRequest equityBuyRequest = new StockPurchaseRequest();
            equityBuyRequest.setBuyer(this.investmentManager.getAssignedCompany());
            equityBuyRequest.setStock(selectedStock);
            equityBuyRequest.setQuantity(Integer.parseInt(txtQtyShare.getText()));
            equityBuyRequest.setPurchaseID(this.business.getBrokerage().getPurchaseRequestCount());
            this.business.getBrokerage().setPurchaseRequestCount(this.business.getBrokerage().getPurchaseRequestCount() + 1);
            StockBroker equityBroker = this.business.getBrokerage().getStockBrokerByName(String.valueOf(jComboBox1.getSelectedItem()));
            equityBroker.getPurchaseRequests().add(equityBuyRequest);
            this.investmentManager.getAssignedCompany().getStockBuyRequests().add(equityBuyRequest);
            JOptionPane.showMessageDialog(this, "Buy Request Sent!");
            setAvailableBalance(this.investmentManager.getAssignedCompany().getBalance());
        } else {
            JOptionPane.showMessageDialog(this, "Not enough balance");
        }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this, "Oops something went wrong");
        }
    }//GEN-LAST:event_btnBuyShare1ActionPerformed

